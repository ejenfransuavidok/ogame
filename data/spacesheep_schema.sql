CREATE TABLE spacesheeps (
    id INTEGER PRIMARY KEY AUTO_INCREMENT, 
    name varchar(256), 
    description text,
    speed INTEGER, 
    capacity INTEGER, 
    fuel_consumption INTEGER, 
    fuel_tank_size INTEGER, 
    attak_power INTEGER, 
    rate_of_fire INTEGER, 
    the_number_of_attak_targets INTEGER,
    sheep_size INTEGER,
    protection INTEGER,
    number_of_guns INTEGER,
    construction_time INTEGER,
    owner INTEGER);
