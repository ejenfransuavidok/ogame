    owner INTEGER DEFAULT NULL,
    level INTEGER DEFAULT NULL,
    produce_metall INTEGER DEFAULT NULL,
    produce_heavygas INTEGER DEFAULT NULL,
    produce_ore INTEGER DEFAULT NULL,
    produce_hydro INTEGER DEFAULT NULL,
    produce_titan INTEGER DEFAULT NULL,
    produce_darkmatter INTEGER DEFAULT NULL,
    produce_redmatter INTEGER DEFAULT NULL,
    produce_anti INTEGER DEFAULT NULL,
    produce_electricity INTEGER DEFAULT NULL,
    consume_metall INTEGER DEFAULT NULL,
    consume_heavygas INTEGER DEFAULT NULL,
    consume_ore INTEGER DEFAULT NULL,
    consume_hydro INTEGER DEFAULT NULL,
    consume_titan INTEGER DEFAULT NULL,
    consume_darkmatter INTEGER DEFAULT NULL,
    consume_redmatter INTEGER DEFAULT NULL,
    consume_anti INTEGER DEFAULT NULL,
    consume_electricity INTEGER DEFAULT NULL);
